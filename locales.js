// ==================== HELPER FUNCTIONS ====================

// Get translation with fallback to English
function L(key, lang = 'en') {
  // Try requested language first
  if (LOCALES[lang] && LOCALES[lang][key]) {
    return LOCALES[lang][key];
  }
  // Fallback to English
  if (LOCALES.en && LOCALES.en[key]) {
    return LOCALES.en[key];
  }
  // Return key if not found
  return key;
}

// Get translation with placeholder replacement
// Usage: Lf('startingTranslation', 'id', { count: 5 })
function Lf(key, lang = 'en', params = {}) {
  let text = L(key, lang);
  for (const [param, value] of Object.entries(params)) {
    text = text.replace(new RegExp(`\\{${param}\\}`, 'g'), value);
  }
  return text;
}

// Get list of supported languages
function getSupportedLanguages() {
  return Object.keys(LOCALES);
}

// Check if language is supported
function isLanguageSupported(lang) {
  return LOCALES.hasOwnProperty(lang);
}
